namespace Remotion.Web.UI.Controls
{
  /// <summary>
  /// Defines how a button is displayed on the page.
  /// </summary>
  public enum ButtonType
  {
    /// <summary>
    /// The button is displayed using the standard style.
    /// </summary>
    Standard,

    /// <summary>
    /// The button is displayed using a style indicating a primary action.
    /// </summary>
    Primary,

    /// <summary>
    /// The button is displayed using a style indicating a supplemental action.
    /// </summary>
    Supplemental
  }
}